/// Records the original index of a child node in its parent's children list,
/// captured during `GroupNodes::apply` for use in undo to restore ordering.
#[derive(Debug, Clone)]
struct ChildOriginalIndex {
    node_id: NodeId,
    index: usize,
}

/// Groups multiple nodes under a new Group node.
///
/// The group's transform is the union bounding box of all selected nodes.
/// Children's coordinates are adjusted to be group-relative.
/// Requires 2+ nodes. All nodes must exist and share the same parent
/// (for the initial implementation; LCA support is deferred).
#[derive(Debug)]
pub struct GroupNodes {
    /// The node IDs to group.
    node_ids: Vec<NodeId>,
    /// The display name for the group node.
    group_name: String,
    /// UUID for the new group node (generated by caller).
    group_uuid: Uuid,
    /// Original indices of children in parent's children list, captured during
    /// apply for use in undo to restore sibling ordering.
    original_child_indices: RefCell<Vec<ChildOriginalIndex>>,
}

impl GroupNodes {
    /// Creates a new `GroupNodes` command.
    ///
    /// # Errors
    /// Returns `CoreError::ValidationError` if:
    /// - The group name is invalid
    /// - Fewer than `MIN_GROUP_MEMBERS` node IDs are provided
    pub fn new(
        node_ids: Vec<NodeId>,
        group_name: String,
        group_uuid: Uuid,
    ) -> Result<Self, CoreError> {
        validate_node_name(&group_name)?;
        if node_ids.len() < MIN_GROUP_MEMBERS {
            return Err(CoreError::ValidationError(format!(
                "grouping requires at least {MIN_GROUP_MEMBERS} nodes, got {}",
                node_ids.len()
            )));
        }
        Ok(Self {
            node_ids,
            group_name,
            group_uuid,
            original_child_indices: RefCell::new(Vec::new()),
        })
    }

    /// Returns the node IDs to group.
    #[must_use]
    pub fn node_ids(&self) -> &[NodeId] {
        &self.node_ids
    }

    /// Returns the group UUID.
    #[must_use]
    pub fn group_uuid(&self) -> Uuid {
        self.group_uuid
    }

    /// Returns the group name.
    #[must_use]
    pub fn group_name(&self) -> &str {
        &self.group_name
    }
}

/// Computes the union bounding box of the given nodes.
fn compute_union_bounds(
    doc: &Document,
    node_ids: &[NodeId],
) -> Result<(f64, f64, f64, f64), CoreError> {
    let mut min_x = f64::INFINITY;
    let mut min_y = f64::INFINITY;
    let mut max_x = f64::NEG_INFINITY;
    let mut max_y = f64::NEG_INFINITY;

    for &nid in node_ids {
        let t = doc.arena.get(nid)?.transform;
        min_x = min_x.min(t.x);
        min_y = min_y.min(t.y);
        max_x = max_x.max(t.x + t.width);
        max_y = max_y.max(t.y + t.height);
    }

    Ok((min_x, min_y, max_x - min_x, max_y - min_y))
}

impl Command for GroupNodes {
    fn apply(&self, doc: &mut Document) -> Result<Vec<SideEffect>, CoreError> {
        // Validate minimum count
        if self.node_ids.len() < MIN_GROUP_MEMBERS {
            return Err(CoreError::ValidationError(format!(
                "grouping requires at least {MIN_GROUP_MEMBERS} nodes, got {}",
                self.node_ids.len()
            )));
        }

        // Pre-validate: all nodes exist, share the same parent, and have a parent (RF-020)
        for &nid in &self.node_ids {
            doc.arena.get(nid)?;
        }

        let first_parent = doc.arena.get(self.node_ids[0])?.parent;
        let first_parent = first_parent.ok_or_else(|| {
            CoreError::ValidationError("cannot group nodes without a parent".to_string())
        })?;

        for &nid in &self.node_ids[1..] {
            let parent = doc.arena.get(nid)?.parent;
            if parent != Some(first_parent) {
                return Err(CoreError::ValidationError(
                    "all nodes must share the same parent for grouping".to_string(),
                ));
            }
        }

        // Pre-validate: record original child indices in parent (RF-001)
        // and verify all children are actually in the parent's children list (RF-007)
        let parent_node = doc.arena.get(first_parent)?;
        let mut child_indices = Vec::with_capacity(self.node_ids.len());
        for &nid in &self.node_ids {
            let idx = parent_node
                .children
                .iter()
                .position(|&id| id == nid)
                .ok_or_else(|| {
                    CoreError::ValidationError(format!(
                        "node {nid:?} is not in parent's children list"
                    ))
                })?;
            child_indices.push(ChildOriginalIndex {
                node_id: nid,
                index: idx,
            });
        }
        *self.original_child_indices.borrow_mut() = child_indices;

        // Compute union bounding box
        let (bx, by, bw, bh) = compute_union_bounds(doc, &self.node_ids)?;
        let group_transform = Transform {
            x: bx,
            y: by,
            width: bw,
            height: bh,
            rotation: 0.0,
            scale_x: 1.0,
            scale_y: 1.0,
        };
        validate_transform(&group_transform)?;

        // Find the topmost (lowest index) selected node in the parent's children
        // to determine insertion position
        let parent = doc.arena.get(first_parent)?;
        let mut insertion_index = parent.children.len();
        for &nid in &self.node_ids {
            if let Some(pos) = parent.children.iter().position(|&id| id == nid) {
                insertion_index = insertion_index.min(pos);
            }
        }

        // Create group node
        let mut group_node = Node::new(
            NodeId::new(0, 0),
            self.group_uuid,
            NodeKind::Group,
            self.group_name.clone(),
        )?;
        group_node.transform = group_transform;
        let group_id = doc.arena.insert(group_node)?;

        // Place group under the shared parent at the topmost position
        tree::rearrange(&mut doc.arena, group_id, first_parent, insertion_index)?;

        // Reparent children into the group, adjusting transforms to group-relative
        for &nid in &self.node_ids {
            let old_transform = doc.arena.get(nid)?.transform;
            let new_transform = Transform {
                x: old_transform.x - bx,
                y: old_transform.y - by,
                ..old_transform
            };
            // Remove from old parent and add to group
            tree::remove_child(&mut doc.arena, nid)?;
            tree::add_child(&mut doc.arena, group_id, nid)?;
            doc.arena.get_mut(nid)?.transform = new_transform;
        }

        Ok(vec![])
    }

    fn undo(&self, doc: &mut Document) -> Result<Vec<SideEffect>, CoreError> {
        // Find the group node by UUID
        let group_id = doc
            .arena
            .id_by_uuid(&self.group_uuid)
            .ok_or(CoreError::ValidationError(format!(
                "cannot undo GroupNodes: group with uuid {} not found",
                self.group_uuid
            )))?;

        let group_transform = doc.arena.get(group_id)?.transform;
        let group_children: Vec<NodeId> = doc.arena.get(group_id)?.children.clone();
        let group_parent = doc.arena.get(group_id)?.parent;

        // RF-017: Validate restored transforms symmetrically
        for &child_id in &group_children {
            let child_transform = doc.arena.get(child_id)?.transform;
            let restored_transform = Transform {
                x: child_transform.x + group_transform.x,
                y: child_transform.y + group_transform.y,
                ..child_transform
            };
            validate_transform(&restored_transform)?;
        }

        // Detach children from the group (but don't attach to parent yet)
        // and restore their absolute coordinates.
        let mut restored_children: Vec<(NodeId, Transform)> =
            Vec::with_capacity(group_children.len());
        for &child_id in &group_children {
            let child_transform = doc.arena.get(child_id)?.transform;
            let restored_transform = Transform {
                x: child_transform.x + group_transform.x,
                y: child_transform.y + group_transform.y,
                ..child_transform
            };
            tree::remove_child(&mut doc.arena, child_id)?;
            doc.arena.get_mut(child_id)?.transform = restored_transform;
            restored_children.push((child_id, restored_transform));
        }

        // Remove the group node first, so it doesn't interfere with index-based rearrange
        tree::remove_child(&mut doc.arena, group_id)?;
        doc.arena.remove(group_id)?;

        // RF-001: Restore children at their original indices in the parent.
        let indices = self.original_child_indices.borrow();
        if let Some(parent_id) = group_parent {
            if indices.is_empty() {
                // Fallback: just add children to parent (shouldn't happen normally)
                for (child_id, _) in &restored_children {
                    tree::add_child(&mut doc.arena, parent_id, *child_id)?;
                }
            } else {
                // Sort by original index (ascending) and insert at those positions.
                let mut sorted_indices: Vec<&ChildOriginalIndex> = indices.iter().collect();
                sorted_indices.sort_by_key(|ci| ci.index);
                for ci in sorted_indices {
                    tree::rearrange(&mut doc.arena, ci.node_id, parent_id, ci.index)?;
                }
            }
        }

        Ok(vec![])
    }

    fn description(&self) -> &str {
        "Group nodes"
    }
}

/// Ungroups one or more group nodes, reparenting their children
/// back to the group's parent.
///
/// Children's coordinates are adjusted from group-relative back to
/// parent-relative (by adding the group's position).
#[derive(Debug)]
#[allow(clippy::struct_field_names)]
pub struct UngroupNodes {
    /// The group node IDs to ungroup.
    group_ids: Vec<NodeId>,
    /// UUIDs of the group nodes (for undo reconstruction).
    group_uuids: Vec<Uuid>,
    /// Captured group snapshots for undo. Populated automatically during `apply()`
    /// via interior mutability (RF-003).
    group_snapshots: RefCell<Vec<GroupSnapshot>>,
}

/// Snapshot of a group node captured before ungrouping, used for undo.
#[derive(Debug, Clone)]
pub struct GroupSnapshot {
    /// UUID of the group node.
    pub uuid: Uuid,
    /// Arena `NodeId` of the group (needed for `reinsert`).
    pub node_id: NodeId,
    /// Full clone of the group `Node`.
    pub node: Node,
    /// The group's parent (if any).
    pub parent_id: Option<NodeId>,
    /// The group's index within its parent's children list.
    pub index_in_parent: usize,
    /// Children node IDs in order, with their pre-ungroup (group-relative) transforms.
    pub children: Vec<(NodeId, Transform)>,
}

impl UngroupNodes {
    /// Creates a new `UngroupNodes` command.
    ///
    /// # Errors
    /// Returns `CoreError::ValidationError` if:
    /// - `group_ids` is empty
    /// - `group_ids` and `group_uuids` have different lengths
    pub fn new(group_ids: Vec<NodeId>, group_uuids: Vec<Uuid>) -> Result<Self, CoreError> {
        if group_ids.is_empty() {
            return Err(CoreError::ValidationError(
                "ungrouping requires at least 1 group node".to_string(),
            ));
        }
        if group_ids.len() != group_uuids.len() {
            return Err(CoreError::ValidationError(format!(
                "group_ids length ({}) must match group_uuids length ({})",
                group_ids.len(),
                group_uuids.len()
            )));
        }
        Ok(Self {
            group_ids,
            group_uuids,
            group_snapshots: RefCell::new(Vec::new()),
        })
    }

    /// Returns the group IDs.
    #[must_use]
    pub fn group_ids(&self) -> &[NodeId] {
        &self.group_ids
    }

    /// Returns the group UUIDs.
    #[must_use]
    pub fn group_uuids(&self) -> &[Uuid] {
        &self.group_uuids
    }
}

impl Command for UngroupNodes {
    fn apply(&self, doc: &mut Document) -> Result<Vec<SideEffect>, CoreError> {
        if self.group_ids.is_empty() {
            return Err(CoreError::ValidationError(
                "ungrouping requires at least 1 group node".to_string(),
            ));
        }

        // Pre-validate: all nodes exist and are Group kind (RF-007)
        for &gid in &self.group_ids {
            let node = doc.arena.get(gid)?;
            if !matches!(node.kind, NodeKind::Group) {
                return Err(CoreError::ValidationError(format!(
                    "node {gid:?} is not a group (kind: {:?})",
                    node.kind
                )));
            }
        }

        // RF-003: Capture snapshots inside apply before destroying groups
        let mut snapshots = Vec::with_capacity(self.group_ids.len());
        for &gid in &self.group_ids {
            let group = doc.arena.get(gid)?;
            let group_parent = group.parent;

            let children_with_transforms: Vec<(NodeId, Transform)> = group
                .children
                .iter()
                .map(|&cid| {
                    let t = doc.arena.get(cid).map(|n| n.transform);
                    t.map(|transform| (cid, transform))
                })
                .collect::<Result<Vec<_>, _>>()?;

            // RF-012: Replace .unwrap_or(0) with error
            let index_in_parent = if let Some(pid) = group_parent {
                doc.arena
                    .get(pid)?
                    .children
                    .iter()
                    .position(|&id| id == gid)
                    .ok_or_else(|| {
                        CoreError::ValidationError(
                            "group not found in parent's children list".to_string(),
                        )
                    })?
            } else {
                0
            };

            let group_uuid = doc.arena.uuid_of(gid)?;

            snapshots.push(GroupSnapshot {
                uuid: group_uuid,
                node_id: gid,
                node: group.clone(),
                parent_id: group_parent,
                index_in_parent,
                children: children_with_transforms,
            });
        }
        *self.group_snapshots.borrow_mut() = snapshots;

        // Process each group
        for &gid in &self.group_ids {
            let group = doc.arena.get(gid)?;
            let group_transform = group.transform;
            let group_parent = group.parent;
            let group_children: Vec<NodeId> = group.children.clone();

            // RF-012: Replace .unwrap_or(0) with error
            let group_index = if let Some(pid) = group_parent {
                doc.arena
                    .get(pid)?
                    .children
                    .iter()
                    .position(|&id| id == gid)
                    .ok_or_else(|| {
                        CoreError::ValidationError(
                            "group not found in parent's children list".to_string(),
                        )
                    })?
            } else {
                0
            };

            // Reparent each child to the group's parent, adjusting coords
            for (i, &child_id) in group_children.iter().enumerate() {
                let child_transform = doc.arena.get(child_id)?.transform;
                let restored = Transform {
                    x: child_transform.x + group_transform.x,
                    y: child_transform.y + group_transform.y,
                    ..child_transform
                };
                // RF-017: Validate restored transforms
                validate_transform(&restored)?;
                tree::remove_child(&mut doc.arena, child_id)?;
                if let Some(pid) = group_parent {
                    tree::rearrange(&mut doc.arena, child_id, pid, group_index + i)?;
                }
                doc.arena.get_mut(child_id)?.transform = restored;
            }

            // Remove the now-empty group
            tree::remove_child(&mut doc.arena, gid)?;
            doc.arena.remove(gid)?;
        }

        Ok(vec![])
    }

    fn undo(&self, doc: &mut Document) -> Result<Vec<SideEffect>, CoreError> {
        let snapshots = self.group_snapshots.borrow();

        // Validate snapshots are available
        if snapshots.is_empty() {
            return Err(CoreError::ValidationError(
                "cannot undo UngroupNodes: no snapshots captured".to_string(),
            ));
        }

        // Restore from snapshots in reverse order
        for snapshot in snapshots.iter().rev() {
            // Reinsert the group node at its original arena slot
            doc.arena
                .reinsert(snapshot.node_id, snapshot.node.clone())?;

            // Re-attach to parent
            if let Some(pid) = snapshot.parent_id {
                tree::rearrange(
                    &mut doc.arena,
                    snapshot.node_id,
                    pid,
                    snapshot.index_in_parent,
                )?;
            }

            // Reparent children back into the group, restoring group-relative coords
            for &(child_id, old_child_transform) in &snapshot.children {
                // RF-017: Validate restored transforms symmetrically
                validate_transform(&old_child_transform)?;
                tree::remove_child(&mut doc.arena, child_id)?;
                tree::add_child(&mut doc.arena, snapshot.node_id, child_id)?;
                doc.arena.get_mut(child_id)?.transform = old_child_transform;
            }
        }

        Ok(vec![])
    }

    fn description(&self) -> &str {
        "Ungroup nodes"
    }
}

