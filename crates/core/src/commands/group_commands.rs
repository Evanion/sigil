    // RF-022: Integration test chaining GroupNodes -> UngroupNodes
    #[test]
    fn test_group_then_ungroup_round_trip() {
        let mut doc = Document::new("Test".to_string());
        let parent = insert_frame(&mut doc, 1, "Parent");
        let r1 = insert_rect(&mut doc, 2, "Rect1", 10.0, 20.0, 50.0, 50.0);
        let r2 = insert_rect(&mut doc, 3, "Rect2", 100.0, 120.0, 80.0, 60.0);
        let r3 = insert_rect(&mut doc, 4, "Rect3", 200.0, 0.0, 40.0, 40.0);

        tree::add_child(&mut doc.arena, parent, r1).unwrap();
        tree::add_child(&mut doc.arena, parent, r2).unwrap();
        tree::add_child(&mut doc.arena, parent, r3).unwrap();

        // Group r1 and r2
        let group_cmd = GroupNodes::new(vec![r1, r2], "Group 1".to_string(), make_uuid(10))
            .expect("create group cmd");
        doc.execute(Box::new(group_cmd)).expect("group");

        let group_id = doc.arena.id_by_uuid(&make_uuid(10)).unwrap();

        // Ungroup
        let ungroup_cmd =
            UngroupNodes::new(vec![group_id], vec![make_uuid(10)]).expect("create ungroup cmd");
        doc.execute(Box::new(ungroup_cmd)).expect("ungroup");

        // All three rects should be under parent with original absolute coords
        assert_eq!(doc.arena.get(r1).unwrap().parent, Some(parent));
        assert_eq!(doc.arena.get(r2).unwrap().parent, Some(parent));
        assert_eq!(doc.arena.get(r3).unwrap().parent, Some(parent));
        assert_eq!(doc.arena.get(r1).unwrap().transform.x, 10.0);
        assert_eq!(doc.arena.get(r2).unwrap().transform.x, 100.0);
        assert_eq!(doc.arena.get(r3).unwrap().transform.x, 200.0);

        // Undo ungroup -- group restored
        doc.undo().expect("undo ungroup");
        let gid = doc.arena.id_by_uuid(&make_uuid(10)).unwrap();
        assert!(matches!(doc.arena.get(gid).unwrap().kind, NodeKind::Group));

        // Undo group -- everything back to original
        doc.undo().expect("undo group");
        assert!(doc.arena.id_by_uuid(&make_uuid(10)).is_none());
        let parent_children = &doc.arena.get(parent).unwrap().children;
        assert_eq!(parent_children.len(), 3);
        assert_eq!(doc.arena.get(r1).unwrap().transform.x, 10.0);
        assert_eq!(doc.arena.get(r2).unwrap().transform.x, 100.0);
        assert_eq!(doc.arena.get(r3).unwrap().transform.x, 200.0);
    }
